<h2>La St-V</h2><br><br>

La Saint-V (diminutif de Saint-Verhaegen) célèbre ldda fondation de l’ULB. Chaque 20 novembre est synonyme de congé pour tous les étudiants de l’ULB. Si cette journée débute par une série de commémorations, elle se poursuit dans la guindaille l’après-midi avec le traditionnel cortège organisé conjointement par l'Association des cercles étudiants (ACE) et son homologue néerlandophone la Brusselse Studentengenootschap (BSG) de la Vrije Universiteit Brussel (VUB).<br><br>




Il se déroule dans le centre-ville, du Sablon jusqu’à la Bourse, et rassemble des chars affrétés par les différents cercles étudiants de l’ULB et de la VUB. Ils arborent décorations illustrant un thème annuel choisi par les étudiants et chacun transporte pompes et fûts de bière afin d’abreuver les quelques milliers d’étudiants et anciens étudiants participants à cette guindaille. Bien évidement, le Cercle Informatique est de la partie alors on t’attend aux abords du char CI pour picoler comme des ours !


