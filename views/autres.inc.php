<h2>Glotte d'or</h2><br><br>

La fin de l'année approche, les examens de juin sont finis, t'as raté la blinde d'examens et tu as du mal à accepter ton échec? Pour mieux avaler la pilule, le CI détient le remède miracle : la Glotte d'or ! Mais attention jeune padawan, la Glotte d'or ne s'offre pas au premier venu, il te faudra la mériter, affronter moult adversaires et venir à bout des nombreux pièges houblonnés qui parsèment le chemin de ta quète avant d'avoir le privilège de la remporter. En clair au cas où tu n'aurais pas compris: il s'agit d'un concours d'affonds.<br><br>

