<h1>Bapteme</h1><br>

Comme chaque année, le Cercle Informatique organise un baptême étudiant.<br><br>


Chacun est bien-sûr libre de choisir de tenter l'expérience ou non. Ce choix n'entraînera aucune brimade ou discrimination par la suite. Par contre, beaucoup de monde passe à coté de cette expérience, et il est malheureux de constater que les raisons invoquées sont rarement les bonnes ! Ce texte a pour but d'apporter certains éclaircissements sur le baptême étudiant de l'ULB, plus particulièrement celui du Cercle Informatique, pour vous permettre de faire votre choix en connaissance de cause.<br><br>

L'erreur principale est de croire que le baptême vous fera échouer dans vos études. C'est faux ! N'importe quel baptisé vous le dira ! Nous avons nous même chaque année l'occasion de vérifier que le taux d'échec n'est pas supérieur chez les étudiants baptisés.<br><br>

En effet, le baptême est aussi une intégration au sein d'un groupe d'étudiants faisant les mêmes études que vous, qui pourront vous guider, conseiller, encourager et motiver à réussir. D'ailleurs, les exemples concrets d'étudiants qui ont réussi brillamment leurs études tout profitant pleinement des guindailles sont nombreux au Cercle Informatique. L'expérience nous a ainsi montré qu'il était tout à fait possible de faire la fête et de s'amuser pendant toute l'année, tout en réussissant ses études sans problèmes !<br><br>



Ensuite, sachez que le baptême est loin d'être inutile ! Il faut se rendre compte que le baptême c'est une expérience qui durera pendant toutes vos études, voir au delà ! Les 2 mois de bleusaille ne sont que l'entrée dans la communauté folklorique dont vous ferez partie pendant des années. C'est une chance que nous avons à l'ULB d'avoir un folklore et autant d'occasions de faire la fête en dehors des cours. Le baptême vous permettra de profiter pleinement de la guindaille qui est si chère à nos yeux. C'est probablement la seule possibilité que vous avez de faire de vos années d'étude les meilleures années de votre vie !
