Cet évènement organisé au second quadrimestre est destiné à mettre en contact les futurs diplômés de la faculté des sciences (les informaticiens, les mathématiciens, les statisticiens) ainsi que de la faculté de sciences appliquées (polytechnique) de l’ULB, mais aussi des Hautes Écoles d’informatique de Bruxelles, avec des professionnels venus présenter les opportunités de carrière au sein de leur entreprise.<br><br>

